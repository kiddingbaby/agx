func TestSnapshotCreateBackupAndRestore(t *testing.T) {
	dir := t.TempDir()
	configPath := filepath.Join(dir, ".codex", "config.toml")
	backupsDir := filepath.Join(dir, ".config", "agx", "backups")
	original := "profile = \"before\"\n"
	if err := os.MkdirAll(filepath.Dir(configPath), 0700); err != nil {
		t.Fatalf("MkdirAll() error = %v", err)
	}
	if err := os.WriteFile(configPath, []byte(original), 0600); err != nil {
		t.Fatalf("WriteFile() error = %v", err)
	}

	syncer := NewSyncer(configPath, backupsDir, "agx")
	snapshot, err := syncer.Snapshot()
	if err != nil {
		t.Fatalf("Snapshot() error = %v", err)
	}
	if !snapshot.Exists || string(snapshot.Content) != original {
		t.Fatalf("Snapshot() = %+v, want existing original content", snapshot)
	}

	backupPath, err := syncer.CreateBackup("before-codex-sync", snapshot.Content)
	if err != nil {
		t.Fatalf("CreateBackup() error = %v", err)
	}
	if _, err := syncer.Sync(domainprofile.Profile{Name: "relay-a", BaseURL: "https://relay.example", APIKey: "sk-a"}, ports.CodexSyncOptions{}); err != nil {
		t.Fatalf("Sync() error = %v", err)
	}
	if _, err := syncer.Restore(backupPath); err != nil {
		t.Fatalf("Restore() error = %v", err)
	}

	got, err := os.ReadFile(configPath)
	if err != nil {
		t.Fatalf("ReadFile(config) error = %v", err)
	}
	if string(got) != original {
		t.Fatalf("restored config = %q, want %q", string(got), original)
	}
}

