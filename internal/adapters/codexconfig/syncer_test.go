func TestSyncUpdatesCodexConfigWithoutBackupSideEffects(t *testing.T) {
	dir := t.TempDir()
	configPath := filepath.Join(dir, ".codex", "config.toml")
	backupsDir := filepath.Join(dir, ".config", "agx", "backups")
	if err := os.MkdirAll(filepath.Dir(configPath), 0700); err != nil {
		t.Fatalf("MkdirAll() error = %v", err)
	}
	original := "model = \"gpt-5.4\"\nprofile = \"old\"\n"
	if err := os.WriteFile(configPath, []byte(original), 0600); err != nil {
		t.Fatalf("WriteFile() error = %v", err)
	}

	syncer := NewSyncer(configPath, backupsDir, "agx")
	result, err := syncer.Sync(domainprofile.Profile{
		Name:      "relay-a",
		BaseURL:   "https://relay.example/v1",
		APIKey:    "sk-a",
		CreatedAt: time.Now(),
		UpdatedAt: time.Now(),
	}, ports.CodexSyncOptions{})
	if err != nil {
		t.Fatalf("Sync() error = %v", err)
	}
	if result.ProfileName != "agx/relay-a" {
		t.Fatalf("ProfileName = %q, want agx/relay-a", result.ProfileName)
	}

	got, err := os.ReadFile(configPath)
	if err != nil {
		t.Fatalf("ReadFile(config) error = %v", err)
	}
	text := string(got)
	if !strings.Contains(text, "profile = \"old\"") {
		t.Fatalf("config=%q want root profile preserved without explicit default", text)
	}
	if !strings.Contains(text, beginMarker) || !strings.Contains(text, "command = \"agx\"") {
		t.Fatalf("config=%q want managed block", text)
	}
	if !strings.Contains(text, "# The rest of this file stays in your original order.") {
		t.Fatalf("config=%q want managed block note", text)
	}
	if !strings.Contains(text, "[model_providers]\n\n[profiles]\n\n[model_providers.\"agx/relay-a\"]\nname = \"relay-a\"") {
		t.Fatalf("config=%q want codex parent tables and provider name", text)
	}
	if !strings.Contains(text, "[profiles.\"agx/relay-a\"]\nmodel_provider = \"agx/relay-a\"") {
		t.Fatalf("config=%q want namespaced relay-a profile block", text)
	}
}

